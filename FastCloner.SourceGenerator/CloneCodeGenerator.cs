using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace FastCloner.SourceGenerator;

/// <summary>
/// Generates FastDeepClone() extension methods for types marked with [FastClonerClonable].
/// </summary>
internal sealed class CloneCodeGenerator
{
    private readonly CloneGeneratorContext _context;
    private readonly EquatableArray<GenericUsage> _usages;

    public CloneCodeGenerator(TypeModel model, EquatableArray<GenericUsage> usages)
    {
        _context = new CloneGeneratorContext(model);
        _usages = usages;
    }

    public string Generate()
    {
        WriteFileHeader();
        WriteUsings();
        WriteNamespace();
        WriteExtensionClass();
        WriteFileFooter();

        return _context.Source.ToString();
    }

    private void WriteFileHeader()
    {
        var sb = _context.Source;
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine($"// Generated: {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
    }

    private void WriteUsings()
    {
        var sb = _context.Source;
        sb.AppendLine("using System;");
        sb.AppendLine("using System.Collections.Generic;");
        sb.AppendLine("using System.Reflection;");

        // Only add FastCloner using if the library is available
        if (_context.IsFastClonerAvailable)
        {
            sb.AppendLine("using FastCloner;");
        }

        sb.AppendLine();
    }

    private void WriteNamespace()
    {
        var ns = _context.Model.Namespace;
        if (!string.IsNullOrEmpty(ns))
        {
            _context.Source.AppendLine($"namespace {ns}");
            _context.Source.AppendLine("{");
        }
    }

    private void WriteExtensionClass()
    {
        // Use the fully qualified name from TypeModel
        var typeName = _context.Model.FullyQualifiedName;
        var fullTypeName = _context.Model.FullyQualifiedName;
        var sb = _context.Source;

        sb.AppendLine("    /// <summary>");
        sb.AppendLine($"    /// Extension methods for cloning {_context.Model.Name}.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine($"    public static partial class {_context.Model.Name}FastDeepCloneExtensions");
        sb.AppendLine("    {");

        // Generate public FastDeepClone() without state parameter
        WritePublicFastDeepCloneMethod(typeName, fullTypeName);

        // Only generate InternalFastDeepClone if circular references are possible
        // (for types without circular refs, the public method inlines the logic directly)
        if (_context.CanHaveCircularReferences)
        {
            WritePrivateFastDeepCloneMethod(typeName, fullTypeName);
        }

        // Generate Cloner<T> helper class (private nested class)
        // Must be generated before helpers to register its dependencies
        WriteClonerClass();

        // Generate helper methods for nested types
        CollectionHelperGenerator.GenerateHelpers(_context);

        // Generate FastCloneState helper class (private nested class)
        WriteFastCloneStateClass();

        sb.AppendLine("    }");
    }

    private void WritePublicFastDeepCloneMethod(string typeName, string fullTypeName)
    {
        var sb = _context.Source;
        sb.AppendLine($"        /// <summary>");
        sb.AppendLine($"        /// Performs deep clone of {_context.Model.Name}. This is a source generated method.");
        sb.AppendLine($"        /// </summary>");
        sb.AppendLine($"        /// <param name=\"source\">The object to clone.</param>");

        var typeParams = GetTypeParametersString();
        var constraints = GetTypeConstraintsString();

        sb.AppendLine($"        public static {typeName}? FastDeepClone{typeParams}(this {typeName}? source){constraints}");
        sb.AppendLine("        {");

        // Optimization: When no circular references are possible, inline the clone logic directly
        // instead of calling through InternalFastDeepClone (saves a method call)
        if (!_context.CanHaveCircularReferences)
        {
            sb.AppendLine("            if (source == null) return null;");
            WriteCloneBody(typeName, fullTypeName, false);
        }
        else
        {
            sb.AppendLine("            return InternalFastDeepClone(source, null);");
        }

        sb.AppendLine("        }");
        sb.AppendLine();
    }

    private void WritePrivateFastDeepCloneMethod(string typeName, string fullTypeName)
    {
        var sb = _context.Source;
        sb.AppendLine($"        /// <summary>");
        sb.AppendLine($"        /// Performs deep clone of {_context.Model.Name} with circular reference tracking.");
        sb.AppendLine($"        /// </summary>");
        sb.AppendLine($"        /// <param name=\"source\">The object to clone.</param>");
        sb.AppendLine($"        /// <param name=\"state\">State for circular reference tracking. If null, a new state is created.</param>");

        var typeParams = GetTypeParametersString();
        var constraints = GetTypeConstraintsString();

        sb.AppendLine($"        internal static {typeName}? InternalFastDeepClone{typeParams}(this {typeName}? source, object? state){constraints}");
        sb.AppendLine("        {");
        sb.AppendLine("            if (source == null) return null;");

        // Only use state if circular references are possible
        if (_context.CanHaveCircularReferences)
        {
            _context.NeedsStateClass = true;
            // Cast state to local type or create new one - allows sharing state across different extension classes
            sb.AppendLine("            var localState = (state as FcGeneratedCloneState) ?? new FcGeneratedCloneState();");

            if (!_context.Model.IsStruct)
            {
                sb.AppendLine("            var known = localState.GetKnownRef(source);");
                sb.AppendLine($"            if (known != null) return ({typeName})known;");
            }

            // Use localState in WriteCloneBody - need to pass it as a variable name
            WriteCloneBody(typeName, fullTypeName, true, "localState");
        }
        else
        {
            // If circular references aren't possible, state can remain null and won't be used
            WriteCloneBody(typeName, fullTypeName, false);
        }

        sb.AppendLine("        }");
        sb.AppendLine();
    }

    private string GetTypeParametersString()
    {
        if (_context.Model.TypeParameters.Count == 0)
            return string.Empty;

        return $"<{string.Join(", ", _context.Model.TypeParameters)}>";
    }

    private string GetTypeConstraintsString()
    {
        if (_context.Model.TypeConstraints.Count == 0)
            return string.Empty;

        return " " + string.Join(" ", _context.Model.TypeConstraints);
    }

    private void WriteCloneBody(string typeName, string fullTypeName, bool useState, string? stateVarName = null)
    {
        if (_context.Model.IsStruct)
        {
            WriteStructCloneBody(typeName, stateVarName ?? "state");
        }
        else
        {
            WriteClassCloneBody(typeName, fullTypeName, useState, stateVarName);
        }
    }

    private void WriteStructCloneBody(string typeName, string stateVarName = "state")
    {
        var sb = _context.Source;
        sb.AppendLine($"            var result = source;");
        sb.AppendLine();

        foreach (var member in _context.Model.Members)
        {
            MemberCloneGenerator.WriteMemberCloning(_context, member, "result", "source", stateVarName);
        }

        sb.AppendLine("            return result;");
    }

    private void WriteClassCloneBody(string typeName, string fullTypeName, bool useState, string? stateVarName = null)
    {
        var sb = _context.Source;
        // Use object initializer syntax instead of MemberwiseClone for better performance
        // Create new instance using object initializer
        sb.AppendLine($"            var result = new {typeName}");
        sb.AppendLine("            {");

        // Generate property/field assignments in object initializer
        var memberAssignments = new List<string>();
        foreach (var member in _context.Model.Members)
        {
            // Pass null if state isn't needed, or use the provided stateVarName (or "state" as default)
            var stateVar = useState ? (stateVarName ?? "state") : "null";
            var assignment = MemberCloneGenerator.GetMemberAssignment(_context, member, "source", stateVar);
            if (!string.IsNullOrEmpty(assignment))
            {
                memberAssignments.Add($"                {assignment}");
            }
        }

        if (memberAssignments.Count > 0)
        {
            sb.AppendLine(string.Join(",\n", memberAssignments));
        }

        sb.AppendLine("            };");
        if (useState)
        {
            var stateVarForAdd = stateVarName ?? "state";
            sb.AppendLine($"            {stateVarForAdd}?.AddKnownRef(source, result);");
        }

        sb.AppendLine();
        sb.AppendLine("            return result;");
    }

    private void WriteFastCloneStateClass()
    {
        // Only generate state class if it's actually needed
        if (!_context.NeedsStateClass)
            return;

        var sb = _context.Source;
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// State for tracking circular references during cloning.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        private class FcGeneratedCloneState");
        sb.AppendLine("        {");
        sb.AppendLine("            private readonly Dictionary<object, object> _knownRefs = new Dictionary<object, object>();");
        sb.AppendLine();
        sb.AppendLine("            public void AddKnownRef(object original, object clone)");
        sb.AppendLine("            {");
        sb.AppendLine("                if (original != null)");
        sb.AppendLine("                {");
        sb.AppendLine("                    _knownRefs[original] = clone;");
        sb.AppendLine("                }");
        sb.AppendLine("            }");
        sb.AppendLine();
        sb.AppendLine("            public object? GetKnownRef(object original)");
        sb.AppendLine("            {");
        sb.AppendLine("                if (original == null) return null;");
        sb.AppendLine("                return _knownRefs.TryGetValue(original, out var clone) ? clone : null;");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
    }

    private void WriteClonerClass()
    {
        if (!_context.NeedsClonerClass)
            return;

        var sb = _context.Source;
        sb.AppendLine();
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Helper class for cloning generic types.");
        sb.AppendLine("        /// </summary>");
        
        var typeParams = GetTypeParametersString();
        var constraints = GetTypeConstraintsString();
        
        sb.AppendLine($"        private static class Cloner{typeParams}{constraints}");
        sb.AppendLine("        {");

        sb.AppendLine("            public static T Clone(T source, object? state)");
        sb.AppendLine("            {");
        sb.AppendLine("                if (source == null) return default;");
        
        // Generate dispatch for known usages (Available even without FastCloner runtime)
        foreach (var usage in _usages)
        {
            // Filter usages relevant to THIS generic type definition
            if (usage.GenericTypeMetadataName != _context.Model.FullyQualifiedName)
                continue;

            // Register dependencies
            foreach (var nested in usage.NestedHelpers)
            {
                _context.GetOrCreateHelperMethodName(nested);
            }
            foreach (var implicitType in usage.ImplicitTypes)
            {
                _context.RegisterImplicitType(implicitType);
                _context.GetOrCreateHelperMethodName(implicitType.FullyQualifiedName);
            }
                
            var argType = usage.ArgumentTypeMetadataName;
            
            if (usage.IsSafe)
            {
                sb.AppendLine($"                if (typeof(T) == typeof({argType})) return (T)(object)source;");
            }
            else if (usage.IsClonable && !string.IsNullOrEmpty(usage.ExtensionClassFQN))
            {
                // Generate dispatch to concrete FastDeepClone
                sb.AppendLine($"                if (typeof(T) == typeof({argType}))");
                sb.AppendLine($"                    return (T)(object)({usage.ExtensionClassFQN}.FastDeepClone(({argType})(object)source)!);");
            }
            else if (usage.CollectionModel != null)
            {
                // Get helper name for the collection type
                MemberModel collectionModel = usage.CollectionModel.Value;
                var helperName = _context.GetOrCreateHelperMethodName(collectionModel);
                
                // Determine if helper needs state
                var needsState = MemberCloneGenerator.MemberNeedsCircularRefTracking(_context, collectionModel);
                
                var callArgs = needsState 
                    ? $"(({argType})(object)source, state as FcGeneratedCloneState)" 
                    : $"(({argType})(object)source)";

                sb.AppendLine($"                if (typeof(T) == typeof({argType}))");
                sb.AppendLine($"                    return (T)(object){helperName}{typeParams}{callArgs};");
            }
            else
            {
                // Check if it is an implicit type itself
                if (_context.TryGetImplicitTypeModel(argType, out var implicitModel))
                {
                     // Generate dispatch to implicit helper
                     var helperName = _context.GetOrCreateHelperMethodName(argType);
                     var needsState = implicitModel.CanHaveCircularReferences && _context.CanHaveCircularReferences;
                     var callArgs = needsState 
                        ? $"(({argType})(object)source, state as FcGeneratedCloneState)" 
                        : $"(({argType})(object)source)";

                     sb.AppendLine($"                if (typeof(T) == typeof({argType}))");
                     sb.AppendLine($"                    return (T)(object){helperName}{typeParams}{callArgs};");
                }
            }
        }
        
        if (_context.IsFastClonerAvailable)
        {
            sb.AppendLine("                // If FastCloner is available, delegate to it for deep cloning");
            sb.AppendLine("                return (T)FastCloner.DeepClone(source);");
        }
        else
        {
            sb.AppendLine("                // Fallback: If FastCloner is not available, we can only safely clone");
            sb.AppendLine("                // types that are effectively safe (value types, strings, etc.) by returning them.");
            sb.AppendLine("                // For other types, this results in a shallow copy/identity return,");
            sb.AppendLine("                // which is the best we can do without the runtime library.");
            sb.AppendLine("                return source;");
        }
        sb.AppendLine("            }");

        sb.AppendLine("        }");
    }

    private void WriteFileFooter()
    {
        if (!string.IsNullOrEmpty(_context.Model.Namespace))
        {
            _context.Source.AppendLine("}");
        }
    }
}
